    public void loadStatus() {
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `production_status`");
            Vector vector = new Vector();
            vector.add("Select");
            while (rs.next()) {
                vector.add(rs.getString("status"));
            }
            DefaultComboBoxModel cbm = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(cbm);

        } catch (Exception e) {
        }
    }

    public void loadTableData() {
        try {
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            ResultSet rs = MySQL.execute("SELECT `product_line`.`id`, `product_line`.`name`, `production_status`.`status` FROM `product_line` INNER JOIN `production_status` ON `product_line`.`production_status_id` = `production_status`.`id` ORDER BY `product_line`.`id`");
            while (rs.next()) {

                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("status"));
                dtm.addRow(v);
            }

            jTable1.setModel(dtm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

