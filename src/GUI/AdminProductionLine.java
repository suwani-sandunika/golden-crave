    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            String id = jTable1.getValueAt(row, 0).toString();
            String status = jTable1.getValueAt(row, 2).toString();

            try {
                ResultSet rs = MySQL.execute("SELECT * FROM `production_status` WHERE `status` != '" + status + "'");
                rs.next();
                String statusId = rs.getString("id");

                MySQL.execute("UPDATE `product_line` SET `production_status_id`='" + statusId + "' WHERE `id`='" + id + "'");

                loadTableData();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            String id = jTable1.getValueAt(row, 0).toString();
            
            try {
                MySQL.execute("DELETE FROM `product_line` WHERE `id`='"+id+"'");
                loadTableData();
                JOptionPane.showMessageDialog(this, "Record deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

