    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField4.getText();
        String status = jComboBox1.getSelectedItem().toString();

        if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Status", "Warming", JOptionPane.WARNING_MESSAGE);
        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please", "Warming", JOptionPane.WARNING_MESSAGE);
        }
        try {
            ResultSet rs = MySQL.execute("SELECT * FROM `production_status` WHERE `status`='" + status + "'");
            rs.next();
            String statusId = rs.getString("id");

            MySQL.execute("INSERT INTO `product_line`(`name`,`production_status_id`)VALUES('" + name + "','" + statusId + "')");

            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);

            loadTableData();

            JOptionPane.showMessageDialog(this, "New Record Added", "success", JOptionPane.INFORMATION_MESSAGE);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

