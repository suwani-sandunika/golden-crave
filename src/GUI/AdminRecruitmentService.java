    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String jobName = jTextField1.getText();
        String payment = jTextField4.getText();

        if (jobName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter job name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (payment.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter payment", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                MySQL.execute("INSERT INTO `job`(`job_type`, `payroll`) VALUES ('" + jobName + "', '" + payment + "')");

                jTextField1.setText("");
                jTextField4.setText("");
                loadJobs();

                JOptionPane.showMessageDialog(this, "New record added", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

