    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (jobId != null) {
            String jobName = jTextField1.getText();
            String payment = jTextField4.getText();

            if (jobName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter job name", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (payment.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please enter payment", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    MySQL.execute("UPDATE `job` SET `job_type` = '" + jobName + "', `payroll` = '" + payment + "' WHERE `id` = '" + jobId + "'");

                    jTextField1.setText("");
                    jTextField4.setText("");
                    loadJobs();

                    JOptionPane.showMessageDialog(this, "Record updated successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    e.printStackTrace();
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            if (row != -1) {
                String id = jTable1.getValueAt(row, 0).toString();
                String job = jTable1.getValueAt(row, 1).toString();
                String payment = jTable1.getValueAt(row, 2).toString();

                jobId = id;
                jTextField1.setText(job);
                jTextField4.setText(payment);
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();

        if (row != -1) {
            String id = jTable1.getValueAt(row, 0).toString();

            try {
                MySQL.execute("DELETE FROM `job` WHERE `id` = '" + id + "'");
                
                loadJobs();
                JOptionPane.showMessageDialog(this, "Record deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton4ActionPerformed

