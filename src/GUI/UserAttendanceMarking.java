        // TODO add your handling code here:                                                                              //Add To Table
        try {
            Date Attendance = jDateChooser1.getDate();
            String datef = new SimpleDateFormat("yyyy-MM-dd").format(Attendance);
            
             ResultSet resultset = MySQL.execute("SELECT * FROM `attendance` INNER JOIN `user` ON `user`.`id`=`attendance`.`user_id` WHERE `user`.`id`='"+userid+"' AND `attendance`.`date`='"+datef+"'");
           
            if (Attendance == null) {
                JOptionPane.showMessageDialog(this, "The Attendance  Field Can Not Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
                // jDateChooser1.requestFocus();

            } else if (datef == null) {
                JOptionPane.showMessageDialog(this, "The Attendance  Field Can Not Be Empty!", "Warning", JOptionPane.WARNING_MESSAGE);
                // jDateChooser1.requestFocus();

            }else {
                 if (resultset.next()) {
                JOptionPane.showMessageDialog(this, " User Attendance  Already  Marked !", "Information", JOptionPane.INFORMATION_MESSAGE);
            }else{
               MySQL.execute("INSERT INTO `attendance` (`date`,`atd_type_id`,`user_id`) VALUES ( '" + datef + "','1','"+userid+"')");                                   //Add 
                JOptionPane.showMessageDialog(this, " User Attendance  Added  Successfully !", "Information", JOptionPane.INFORMATION_MESSAGE);
                loadAttendance(); 
            }

                

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
