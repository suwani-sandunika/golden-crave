    private void loadAttendance() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `attendance` INNER JOIN `atd_type` ON `atd_type`.`id`=`attendance`.`atd_type_id` INNER JOIN `user` ON `user`.`id`=`attendance`.`user_id` WHERE `user`.`id`='"+userid+"'");
            while (resultset.next()) {

                Vector vector = new Vector();
                vector.add(resultset.getString("date"));
                vector.add(resultset.getString("atd_type.type"));

                model.addRow(vector);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

