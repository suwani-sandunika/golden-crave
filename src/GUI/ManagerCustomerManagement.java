    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        
       String fname = jTextField2.getText();
        String lname = jTextField3.getText();
        String mobile = jTextField1.getText();
        String address = jTextField4.getText();
        String password = String.valueOf(jPasswordField1.getPassword());
        String points = jTextField5.getText();

          if (fname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter First Name", "Warning", JOptionPane.WARNING_MESSAGE);
           
                } else if (lname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
            
                }else  if (mobile.isEmpty()) {
                 JOptionPane.showMessageDialog(this, "Please Enter Customer Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
          
                 } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
                 JOptionPane.showMessageDialog(this, "Invaild mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        
               
                 } else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Address", "Warning", JOptionPane.WARNING_MESSAGE);
           
                 } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Password", "Warning", JOptionPane.WARNING_MESSAGE);
           
                  } else if (points.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Points", "Warning", JOptionPane.WARNING_MESSAGE);
           
           } else {
            try {
                   MySQL.execute("INSERT INTO `customer` (`fname`,`lname`,`mobile`,`password`,`address`,`points`) "
                    + "VALUES('" + fname + "','" + lname + "','" + mobile + "','" + password + "','" + address + "','" + points + "')");

                    JOptionPane.showMessageDialog(this, "Registerd", "Success", JOptionPane.WARNING_MESSAGE);
                    reset();
                    loadCustomers();
                
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton5ActionPerformed

