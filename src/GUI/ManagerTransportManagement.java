      private void loadWarehouse() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM `warehouse`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("name")); 
                warehouseMap.put(resultset.getString("name"), resultset.getString("id"));

            }

            jComboBox1.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

     private void loadTransport() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM `transport`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("number_plate"));
                vehicleMap.put(resultset.getString("number_plate"), resultset.getString("id"));

            }

            jComboBox2.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
     
     private void loadTable() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `transport_service` INNER JOIN "
                    + "`transport` ON `transport`.`id`=`transport_service`.`transport_id` INNER JOIN "
                    + "`warehouse` ON  `warehouse`.`id`=`transport_service`.`warehouse_id`");
            while (resultset.next()) {

                Vector vector = new Vector();
                vector.add(resultset.getString("warehouse.name"));
                vector.add(resultset.getString("transport.number_plate"));
                

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }
     
