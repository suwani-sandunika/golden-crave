    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
                 String warehouse = String.valueOf(jComboBox1.getSelectedItem());
        String vehicle = String.valueOf(jComboBox2.getSelectedItem());
        

        if (warehouse.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Warehouse", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (vehicle.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Vehicle", "Warning", JOptionPane.WARNING_MESSAGE);

        }  else {

            try {

//                    MySQL.execute("UPDATE `customer` SET `points`='" + point + "' WHERE `id`='" + customerMap.get(customer) + "'");

                    MySQL.execute("INSERT INTO `transport_service`(`transport_id`,`warehouse_id`) VALUES('" +vehicleMap.get(vehicle) + "','" + warehouseMap.get(warehouse) + "')");
                    JOptionPane.showMessageDialog(this, "Transportation Added!", "Warning", JOptionPane.WARNING_MESSAGE);
                    loadTable();
                    
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        
    }//GEN-LAST:event_jButton5ActionPerformed

