    private void loadExpensetype() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM `expenses_type`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("type"));
                typeMap.put(resultset.getString("type"), resultset.getString("id"));

            }

            jComboBox1.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadExpense() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `expenses` INNER JOIN `expenses_type` ON `expenses_type`.`id`=`expenses`.`expenses_type_id`");
            while (resultset.next()) {

                Vector vector = new Vector();
                vector.add(resultset.getString("id"));
                vector.add(resultset.getString("expenses_type.type"));
                vector.add(resultset.getString("total"));
                vector.add(resultset.getString("date"));

                model.addRow(vector);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void calculate() {

        try {

            ResultSet resultset = MySQL.execute("SELECT SUM(`total`) FROM `expenses`");
            if (resultset.next()) {
                jLabel7.setText(resultset.getString(1));
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

