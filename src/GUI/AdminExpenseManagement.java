    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String type = String.valueOf(jComboBox1.getSelectedItem());
        String expense = jTextField4.getText();

        if (type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Type", "Warning", JOptionPane.WARNING_MESSAGE);

        } else if (expense.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please add Expense ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {

                DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");
                LocalDateTime now = LocalDateTime.now();
                String date = dtf.format(now);

                //  MySQL.execute("UPDATE `customer` SET `points`='" + point + "' WHERE `id`='" + customerMap.get(customer) + "'");
                MySQL.execute("INSERT INTO `expenses`(`total`,`expenses_type_id`,`date`) VALUES('" + expense + "','" + typeMap.get(type) + "','" + date + "')");
                JOptionPane.showMessageDialog(this, "Expense Added", "Warning", JOptionPane.WARNING_MESSAGE);
                loadExpense();
                calculate();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

