    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {

            if (jDateChooser1.getDate() == null) {
                JOptionPane.showMessageDialog(this, "Please Select a Date", "Warning", JOptionPane.WARNING_MESSAGE);

            } else {
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.setRowCount(0);

                double fulltotal = 0;

                Date date = jDateChooser1.getDate();
                String sortDate = new SimpleDateFormat("yyyy-MM-dd").format(date);

                ResultSet resultset = MySQL.execute("SELECT * FROM `attendance` WHERE `date`='" + sortDate + "'");
                while (resultset.next()) {


                    ResultSet resultset2 = MySQL.execute("SELECT * FROM `user` WHERE `id`='" + resultset.getString("user_id") + "'");
                    ResultSet resultset3 = MySQL.execute("SELECT * FROM `atd_type` WHERE `id`='" + resultset.getString("atd_type_id") + "'");

                    resultset2.next();
                    resultset3.next();

                    String fname = resultset2.getString("fname");
                    String lname = resultset2.getString("lname");

                    Vector vector = new Vector();
                    vector.add(resultset.getString("id"));
                    vector.add(fname + " " + lname);
                    vector.add(resultset.getString("date"));
                    vector.add(resultset3.getString("type"));

                    model.addRow(vector);

                }

//                jLabel7.setText(String.valueOf(fulltotal));
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

