    private void loadAttendance() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM `atd_type`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("type"));
                attendanceMap.put(resultset.getString("type"), resultset.getString("id"));

            }

            jComboBox2.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadTable() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `attendance`");
            while (resultset.next()) {

                ResultSet resultset2 = MySQL.execute("SELECT * FROM `user` WHERE `id`='" + resultset.getString("user_id") + "'");
                ResultSet resultset3 = MySQL.execute("SELECT * FROM `atd_type` WHERE `id`='" + resultset.getString("atd_type_id") + "'");

                resultset2.next();
                resultset3.next();

                String fname = resultset2.getString("fname");
                String lname = resultset2.getString("lname");

                Vector vector = new Vector();
                vector.add(resultset.getString("id"));
                vector.add(fname + " " + lname);
                vector.add(resultset.getString("date"));
                vector.add(resultset3.getString("type"));

                model.addRow(vector);

            }

//                jLabel7.setText(String.valueOf(fulltotal));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

