    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
               
        
        String stock = String.valueOf(jComboBox3.getSelectedItem());
        String rate = String.valueOf(jComboBox2.getSelectedItem());
        

        if (stock.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Select a Stock", "Warning", JOptionPane.WARNING_MESSAGE);

        }else if (rate.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please add a Rate ", "Warning", JOptionPane.WARNING_MESSAGE);

        }  else {

            try {
               
                     MySQL.execute("UPDATE `stock` SET `quality_id`='" + rateMap.get(rate) + "' WHERE `id`='" + stock + "'");

                   loadTable();
                    JOptionPane.showMessageDialog(this, "Stock Added", "Warning", JOptionPane.INFORMATION_MESSAGE);

                

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton5ActionPerformed

