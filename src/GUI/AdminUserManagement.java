    private void loadJob() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM job");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("job_type"));
                jobMap.put(resultset.getString("job_type"), resultset.getString("id"));

            }

            jComboBox1.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadStatus() {

        try {

            ResultSet resultset = MySQL.execute("SELECT * FROM status");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultset.next()) {
                vector.add(resultset.getString("name"));
                statusMap.put(resultset.getString("name"), resultset.getString("id"));

            }

            jComboBox2.setModel(new DefaultComboBoxModel<>(vector));

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void loadTable() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM user INNER JOIN job ON job.id=user.job_id INNER JOIN status ON status.id=user.status_id");
            while (resultset.next()) {

                String fname = resultset.getString("user.fname");
                String lname = resultset.getString("user.lname");

                Vector vector = new Vector();
                vector.add(resultset.getString("id"));
                vector.add(fname + " " + lname);
                vector.add(resultset.getString("user.mobile"));
                vector.add(resultset.getString("user.nic"));
                vector.add(resultset.getString("user.password"));
                vector.add(resultset.getString("job.job_type"));
                vector.add(resultset.getString("status.name"));

                model.addRow(vector);

            }

//                jLabel7.setText(String.valueOf(fulltotal));
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

