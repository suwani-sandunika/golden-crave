    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String job = String.valueOf(jComboBox1.getSelectedItem());
        String status = String.valueOf(jComboBox2.getSelectedItem());

        String fname = jTextField1.getText();
        String lname = jTextField2.getText();
        String mobile = jTextField3.getText();
        String nic = jTextField4.getText();
        String address = jTextField5.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        if (fname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your First Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (lname.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Last Name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
            JOptionPane.showMessageDialog(this, "Invaild mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (nic.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your NIC", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter Your Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter A Password", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (job.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Enter Select Job Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please Enter Select Job Type", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                //  MySQL.execute("UPDATE customer SET points='" + point + "' WHERE id='" + customerMap.get(customer) + "'");
                MySQL.execute("INSERT INTO user(fname,lname,mobile,address,nic,password,status_id,job_id) VALUES('" + fname + "','" + lname + "','" + mobile + "','" + address + "','" + nic + "','" + password + "','" + statusMap.get(status) + "','" + jobMap.get(job) + "')");
                JOptionPane.showMessageDialog(this, "User Added", "Warning", JOptionPane.WARNING_MESSAGE);
                loadTable();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

