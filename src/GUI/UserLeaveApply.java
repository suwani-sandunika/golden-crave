      private void loadLeave() {

        try {

            DefaultTableModel model = (DefaultTableModel) jTable3.getModel();
            model.setRowCount(0);

            ResultSet resultset = MySQL.execute("SELECT * FROM `user_leave` WHERE `user_id`='"+userid+"'");
            while (resultset.next()) {



                Vector vector = new Vector();
                vector.add(resultset.getString("id"));
                vector.add(resultset.getString("reason"));
              

                model.addRow(vector);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
      }
      
