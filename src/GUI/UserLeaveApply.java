    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
   //update     
        
       String reason = String.valueOf(jTextArea1.getText());
            
             

     if (reason.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please edit a reason ", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {

            try {
               
                ResultSet resultset = MySQL.execute("SELECT * FROM `user_leave` WHERE `user_id`='"+userid+"' AND `reason`='"+reason1+"'");

                if (resultset.next()) {

                    MySQL.execute("UPDATE `user_leave` SET `reason`='" + reason + "' WHERE `id`='" + resultset.getString("id") + "'");
                    loadLeave();
                    reset();
                    JOptionPane.showMessageDialog(this, " Leave Updated  Successfully!", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    JOptionPane.showMessageDialog(this, "Error", "Warning", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }
     }
    }//GEN-LAST:event_jButton4ActionPerformed

