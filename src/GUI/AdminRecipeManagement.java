    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField4.getText();
        String recipe = jTextArea1.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter recipe name", "warning", JOptionPane.WARNING_MESSAGE);
        } else if (recipe.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please Enter recipe", "warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                MySQL.execute("INSERT INTO `recipe`(`name` , `goods`)VALUES('" + name + "' , '" + recipe + "')");

                jTextField4.setText("");
                jTextArea1.setText("");
                loadRecipe();
                JOptionPane.showMessageDialog(this, "new recipe added", "success", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {

                e.printStackTrace();
            }

        }
    }//GEN-LAST:event_jButton2ActionPerformed

