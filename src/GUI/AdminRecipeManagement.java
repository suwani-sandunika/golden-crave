    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            int row = jTable1.getSelectedRow();
            if (row != -1) {
                String id = jTable1.getValueAt(row, 0).toString();
                String name = jTable1.getValueAt(row, 1).toString();
                String recipe = jTable1.getValueAt(row, 2).toString();
                jTextField4.setText(name);
                jTextArea1.setText(recipe);
                recipeId = id;
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if (recipeId != null) {
            String name = jTextField4.getText();
            String recipe = jTextArea1.getText();

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter recipe name", "warning", JOptionPane.WARNING_MESSAGE);
            } else if (recipe.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter recipe", "warning", JOptionPane.WARNING_MESSAGE);
            } else {
                try {
                    MySQL.execute("UPDATE `recipe` SET `name`='" + name + "', `goods`='" + recipe + "' WHERE `id`='" + recipeId + "'");
                    loadRecipe();
                    jTextField4.setText("");
                    jTextArea1.setText("");
                    recipeId = null;
                    JOptionPane.showMessageDialog(this, "Recipe updated successfully!", "success", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            String id = jTable1.getValueAt(row, 0).toString();

            try {
                MySQL.execute("DELETE FROM `recipe` WHERE `id`='" + id + "'");
                loadRecipe();
                JOptionPane.showMessageDialog(this, "Record deleted successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton4ActionPerformed

